#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t m)
{
	size_t i1;
	size_t i2;

	i1 = 0;
	i2 = 0;
	while (s1[i1])
		i1++;
	while (s2[i2] && i2 < m)
	{
		s1[i1 + i2] = s2[i2];
		i2++;
	}
	s1[i1 + i2] = 0;
	return (s1);
}
